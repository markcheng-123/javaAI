package com.itheima.Array.loop;

public class ForDemo1 {
    public static void main(String[] args) {
        test1();
        //目标:掌握for循环的书写,循环流程,以及for循环的语法特点.
    }

    public static void test1() {
        //需求:使用for循环打印三遍Hala Madrid!
        for (int i = 0; i < 3; i++) {
            System.out.println("Hala Madrid!");
        }
    }
}
